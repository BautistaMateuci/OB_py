

def suma(a, b):
    resultado = a + b
    print(f'El resultado de {a} + {b} es {resultado}')

def resta(a, b):
    resultado = a - b
    print(f'El resultado de {a} - {b} es {resultado}')

def multiplicacion(a, b):
    resultado = a * b
    print(f'El resultado de {a} x {b} es {resultado}')

def division(a, b):
    resultado = a / b
    print(f'El resultado de {a} / {b} es {resultado}')
    
